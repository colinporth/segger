WCHAR ff_convert (WCHAR chr, UINT dir) {

	WCHAR c;
	if (chr < 0x80) /* ASCII */
		c = chr;
	else {
		if (dir) {    /* OEM code to Unicode */
			c = (chr >= 0x100) ? 0 : Tbl[chr - 0x80];
			}
		else {    /* Unicode to OEM code */
			for (c = 0; c < 0x80; c++) {
				if (chr == Tbl[c])
					break;
				}
			c = (c + 0x80) & 0xFF;
			}
		}

	return c;
	}
